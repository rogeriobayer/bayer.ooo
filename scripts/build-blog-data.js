const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');

const BLOG_DIR = path.join(process.cwd(), 'content', 'blog');
const OUTPUT_FILE = path.join(process.cwd(), 'src', 'app', 'lib', 'blog-data.js');

function calculateReadingTime(content) {
  const wordsPerMinute = 200;
  const words = content.trim().split(/\s+/).length;
  return Math.ceil(words / wordsPerMinute);
}

function readPostFile(filePath) {
  const fileContents = fs.readFileSync(filePath, 'utf8');
  const { data, content } = matter(fileContents);
  return {
    slug: data.slug || path.basename(filePath).replace(/\.\w+\.md$/, ''),
    title: data.title || '',
    date: data.date || '',
    lang: data.lang || 'pt',
    excerpt: data.excerpt || '',
    tags: data.tags || [],
    cover: data.cover || null,
    author: data.author || 'Rogério Bayer',
    content,
    readingTime: calculateReadingTime(content),
  };
}

function buildBlogData() {
  if (!fs.existsSync(BLOG_DIR)) {
    console.warn('Blog directory not found, generating empty data');
    fs.writeFileSync(
      OUTPUT_FILE,
      `// Auto-generated by scripts/build-blog-data.js\nexport const blogPosts = [];\n`
    );
    return;
  }

  const files = fs.readdirSync(BLOG_DIR).filter((file) => file.endsWith('.md'));
  const postsBySlug = {};

  for (const file of files) {
    const match = file.match(/^(.+)\.(pt|en|fr)\.md$/);
    if (!match) continue;

    const [, slug, lang] = match;
    const post = readPostFile(path.join(BLOG_DIR, file));

    if (!postsBySlug[slug]) {
      postsBySlug[slug] = { slug, translations: {} };
    }
    postsBySlug[slug].translations[lang] = post;
  }

  const posts = Object.values(postsBySlug).sort((a, b) => {
    const dateA = new Date(a.translations.pt?.date || 0);
    const dateB = new Date(b.translations.pt?.date || 0);
    return dateB - dateA;
  });

  const fileContent = `// Auto-generated by scripts/build-blog-data.js
// Do not edit manually

export const blogPosts = ${JSON.stringify(posts, null, 2)};
`;

  fs.writeFileSync(OUTPUT_FILE, fileContent);
  console.log(`Generated blog data with ${posts.length} posts`);
}

buildBlogData();
