const fs = require('fs');
const path = require('path');

const CONTENT_DIR = path.join(__dirname, '..', 'content');
const LIB_DIR = path.join(__dirname, '..', 'src', 'app', 'lib');

const LOCALES = ['pt', 'en', 'fr'];
const DEFAULT_LOCALE = 'pt';

/**
 * Recursively resolves i18n inline objects into a single locale.
 * If a value is { pt: "...", en: "...", fr: "..." }, returns value[locale] or value[DEFAULT_LOCALE].
 * If a value is an array, maps recursively.
 * If a value is an object (but not i18n), processes recursively.
 * Otherwise returns as-is.
 */
function resolveLocale(obj, locale) {
  if (obj === null || typeof obj !== 'object') {
    return obj;
  }

  if (Array.isArray(obj)) {
    return obj.map((item) => resolveLocale(item, locale));
  }

  // Check if this object is an i18n inline object
  const keys = Object.keys(obj);
  const isI18n = keys.length > 0 && keys.every((k) => LOCALES.includes(k));

  if (isI18n) {
    return obj[locale] ?? obj[DEFAULT_LOCALE] ?? keys[0];
  }

  // Regular object: process each property recursively
  const result = {};
  for (const key of keys) {
    result[key] = resolveLocale(obj[key], locale);
  }
  return result;
}

function buildDataFile(sourcePath, outputPath, exportName) {
  const raw = fs.readFileSync(sourcePath, 'utf-8');
  const data = JSON.parse(raw);

  const localized = {};
  for (const locale of LOCALES) {
    localized[locale] = resolveLocale(data, locale);
  }

  const jsContent = `// Auto-generated by scripts/build-cms-data.js
// Do not edit manually

export const ${exportName} = ${JSON.stringify(localized, null, 2)};
`;

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, jsContent, 'utf-8');

  console.log(`✅ Generated ${path.basename(outputPath)}`);
}

function main() {
  console.log('Building CMS data files...\n');

  try {
    buildDataFile(
      path.join(CONTENT_DIR, 'projects', 'projects.json'),
      path.join(LIB_DIR, 'projects-data.js'),
      'projectsData'
    );

    buildDataFile(
      path.join(CONTENT_DIR, 'career', 'career.json'),
      path.join(LIB_DIR, 'career-data.js'),
      'careerData'
    );

    buildDataFile(
      path.join(CONTENT_DIR, 'extensions', 'extensions.json'),
      path.join(LIB_DIR, 'extensions-data.js'),
      'extensionsData'
    );

    console.log('\n🎉 All CMS data files built successfully!');
  } catch (error) {
    console.error('\n❌ Error building CMS data files:', error.message);
    process.exit(1);
  }
}

main();
